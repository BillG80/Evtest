# I2C Bus addresses
I2C_BUS_0 = 0  # For GY63
I2C_BUS_1 = 1  # For GY91

# Device Addresses
MS5611_ADDR: int = 0x77    # GY63
MPU9250_ADDR = 0x68   # GY91 IMU
BMP280_ADDR = 0x76    # GY91 Barometer

# MS5611 Commands
MS5611_RESET: int = 0x1E
MS5611_CONVERT_D1: int = 0x40
MS5611_CONVERT_D2: int = 0x50
MS5611_ADC_READ: int = 0x00
MS5611_PROM_READ: int = 0xA0

# MS5611 OSR (Oversampling Ratio) constants
MS5611_OSR_256: int = 0x00    # 0.6ms conversion time
MS5611_OSR_512: int = 0x02    # 1.2ms conversion time
MS5611_OSR_1024: int = 0x04   # 2.3ms conversion time
MS5611_OSR_2048: int = 0x06   # 4.6ms conversion time
MS5611_OSR_4096: int = 0x08   # 9.1ms conversion time

# MPU9250 Registers
MPU9250_WHO_AM_I = 0x75
MPU9250_PWR_MGMT_1 = 0x6B
MPU9250_CONFIG = 0x1A
MPU9250_GYRO_CONFIG = 0x1B
MPU9250_ACCEL_CONFIG = 0x1C
MPU9250_ACCEL_XOUT_H = 0x3B
MPU9250_GYRO_XOUT_H = 0x43
MPU9250_INT_PIN_CFG = 0x37

# BMP280 Registers
BMP280_CHIP_ID = 0x58
BMP280_RESET_REG = 0xE0
BMP280_RESET_VALUE = 0xB6
BMP280_STATUS_REG = 0xF3
BMP280_CTRL_MEAS_REG = 0xF4
BMP280_CONFIG_REG = 0xF5
BMP280_PRESS_MSB = 0xF7
BMP280_PRESS_LSB = 0xF8
BMP280_PRESS_XLSB = 0xF9
BMP280_TEMP_MSB = 0xFA
BMP280_TEMP_LSB = 0xFB
BMP280_TEMP_XLSB = 0xFC

# BMP280 Calibration Registers
